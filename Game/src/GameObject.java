
public class GameObject {
	
	private String mName = null;
	private int mAge = -1;
	
	public String getName() {
		return mName;
	}
	
	public void setName(String mName) {
		this.mName = mName;
	}
	
	public int getAge() {
		return mAge;
	}
	
	public void setAge(int mAge) {
		this.mAge = mAge;
	}
	
	

}
